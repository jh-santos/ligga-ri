<script async>
    $(document).ready(function() {
        $('.menu_02 .bloco-2 .drop').on('click', function() {
            if (!$(this).hasClass('ativo')) {
                var temp = $(this),
                    temp1 = temp.find('.drop-item').outerHeight(true);
                $('.menu_02 .bloco-2 .drop-container').css('height', '0');
                $('.menu_02 .bloco-2 .drop').removeClass('ativo');
                temp.find('.drop-container').css('height', temp1);
                temp.addClass('ativo');
                $('.menu_02 .overlay').addClass('ativo');
            } else {
                $('.menu_02 .bloco-2 .drop-container').css('height', '0');
                $('.menu_02 .bloco-2 .drop').removeClass('ativo');
                $('.menu_02 .overlay').removeClass('ativo');
            }
        });
        $('.menu_02 .overlay').on('click', function() {
            $('.menu_02 .bloco-2 .drop-container').css('height', '0');
            $('.menu_02 .bloco-2 .drop').removeClass('ativo');
            $('.menu_02 .overlay').removeClass('ativo');
            // retira ativa se opcional
            $('.menu_02 .bloco-3 .grupo-1 .busca .form-input').val('');
            $('.menu_02 .bloco-2').removeClass('disabled');
            $('.menu_02 .bloco-3 .grupo-1').removeClass('ativo');
        })
        $('.menu_02 .bloco-3 .btn-2').on('click', function() {

            var temp = $(this).attr('busca');
            if (temp == 'open') {
                $('.menu_02 .bloco-2').addClass('disabled');
                $('.menu_02 .bloco-3 .grupo-1').addClass('ativo');
                $('.menu_02 .bloco-3 .grupo-1 .busca .form-input').val('');
                $('.menu_02 .bloco-3 .grupo-1 .busca .form-input').focus();
                $('.menu_02 .overlay').addClass('ativo');
            } else if (temp == 'close') {
                $('.menu_02 .bloco-3 .grupo-1 .busca .form-input').val('');
                $('.menu_02 .bloco-2').removeClass('disabled');
                $('.menu_02 .bloco-3 .grupo-1').removeClass('ativo');
                $('.menu_02 .overlay').removeClass('ativo');
            }

        });

    });
    window.addEventListener('load', function() {

    });
</script>